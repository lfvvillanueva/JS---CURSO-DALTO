// Declaración de una variable numérica
var edad = 25;

// Declaración de una variable de cadena de texto
let nombre = "Juan";

// Declaración de una variable booleana
const activo = true;

// Declaración de una variable de arreglo
let numeros = [1, 2, 3, 4, 5];

// Declaración de una variable de objeto
const persona = {
  nombre: "Juan",
  edad: 25,
};

// Imprimir los valores de las variables en la consola
console.log("Edad:", edad);
console.log("Nombre:", nombre);
console.log("Activo:", activo);
console.log("Números:", numeros);
console.log("Persona:", persona);

// Modificar el valor de una variable numérica
edad = 30;
console.log("Nueva edad:", edad);

// Modificar el valor de una propiedad de un objeto
persona.edad = 30;
console.log("Nueva edad de la persona:", persona.edad);

// Agregar un nuevo número al arreglo
numeros.push(6);
console.log("Números actualizados:", numeros);
